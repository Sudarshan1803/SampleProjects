package com.ing.sfdcIntegration.bean;

public class PartyDataBean {
	private String GridId;
	private String partyName;
	private String privInd;
	private String partyStatus;
	private String Street;
	private String FlatNo;
	private String City;
	private String CIBIL;
	private String CreditScore;
	private String Designation;
	private String Role;

	public String getGridId() {
		return GridId;
	}

	public void setGridId(String gridId) {
		GridId = gridId;
	}

	public String getPartyName() {
		return partyName;
	}

	public void setPartyName(String partyName) {
		this.partyName = partyName;
	}

	public String getPrivInd() {
		return privInd;
	}

	public void setPrivInd(String privInd) {
		this.privInd = privInd;
	}

	public String getPartyStatus() {
		return partyStatus;
	}

	public void setPartyStatus(String partyStatus) {
		this.partyStatus = partyStatus;
	}

	public String getStreet() {
		return Street;
	}

	public void setStreet(String street) {
		Street = street;
	}

	public String getFlatNo() {
		return FlatNo;
	}

	public void setFlatNo(String flatNo) {
		FlatNo = flatNo;
	}

	public String getCity() {
		return City;
	}

	public void setCity(String city) {
		City = city;
	}

	public String getCIBIL() {
		return CIBIL;
	}

	public void setCIBIL(String cIBIL) {
		CIBIL = cIBIL;
	}

	public String getCreditScore() {
		return CreditScore;
	}

	public void setCreditScore(String creditScore) {
		CreditScore = creditScore;
	}

	public String getDesignation() {
		return Designation;
	}

	public void setDesignation(String designation) {
		Designation = designation;
	}

	public String getRole() {
		return Role;
	}

	public void setRole(String role) {
		Role = role;
	}

}
